poke_load_sprites = function(item)
  local sprite_info = PokemonSprites[item.name]
  local sprite = nil
  local new_pos = {}
  if sprite_info then
    sprite = pokermon_config.pokemon_altart and sprite_info.alt or sprite_info.base
    local position = sprite.pos.x and sprite.pos or nil
    if not position then
      position = sprite.pos[math.random(#sprite.pos)]
      new_pos.x = position.x
      new_pos.y = position.y
      item.poke_multi_sprite = true
    else
      new_pos.x = position.x
      new_pos.y = position.y
    end
    local soul_position = sprite.soul_pos
    
    if position then item.pos = new_pos end
    if soul_position then item.soul_pos = soul_position end
  end
end

poke_get_atlas_prefix = function(name, sprite_info)
  local atlas_prefix = nil
  if pokermon_config.pokemon_spritesheet_overrides[name] then return pokermon_config.pokemon_spritesheet_overrides[name] end
  if sprite_info.alts and sprite_info.alts[pokermon_config.pokemon_spritesheet_atlas] then
    atlas_prefix = pokermon_config.pokemon_spritesheet_atlas
  else
    atlas_prefix = "AtlasJokersBasic"
  end
  return atlas_prefix
end

poke_load_atlas = function(item)
  if not item.poke_custom_atlas and PokemonSprites[item.name] then
    local sprite_info = PokemonSprites[item.name]
    local atlas_prefix = poke_get_atlas_prefix(item.name, sprite_info)
    if sprite_info.alts and sprite_info.alts[atlas_prefix] and sprite_info.alts[atlas_prefix].artist then
      item.artist = sprite_info.alts[atlas_prefix].artist
      if sprite_info.alts[atlas_prefix].soul_pos then
        item.soul_pos = sprite_info.alts[atlas_prefix].soul_pos
      end
      if sprite_info.alts[atlas_prefix].anim_atlas then
        item.animated = true
        item.atlas = sprite_info.alts[atlas_prefix].anim_atlas
        item.pos = {x = 0, y = 0}
      end
    end
    if not item.animated then
      if sprite_info.gen_atlas and (type(sprite_info.gen_atlas) == 'number' or item.gen) then
        local gen = type(sprite_info.gen_atlas) == 'number' and sprite_info.gen_atlas or item.gen
        local gen_string = nil
        if gen < 10 then
          gen_string = 'Gen0'..gen
        else
          gen_string = 'Gen'..gen
        end
        item.atlas = atlas_prefix..gen_string
      elseif sprite_info.others_atlas then
        item.atlas = atlas_prefix.."Others"
      else
        item.atlas = atlas_prefix.."Natdex"
      end
      if sprite_info.lookup_gen_atlas and (type(sprite_info.lookup_gen_atlas) == 'number' or item.gen) then
        local gen = type(sprite_info.lookup_gen_atlas) == 'number' and sprite_info.lookup_gen_atlas or item.gen
        local gen_string = nil
        if gen < 10 then
          gen_string = 'Gen0'..gen
        else
          gen_string = 'Gen'..gen
        end
        item.poke_lookup_atlas = atlas_prefix..gen_string
      end
    end
  end
end

